    /**
     * Display a listing of the resource.
     */
    public function index()
    {
        $products = Product::with(['category', 'collection'])->latest()->paginate(20);

        return view('admin.products.index', compact('products'));
    }

    /**
     * Show the form for creating a new resource.
     */
    public function create()
    {
        $categories = Category::where('is_active', true)->get();
        $collections = Collection::where('is_active', true)->get();

        return view('admin.products.create', compact('categories', 'collections'));
    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $validated_data = $request->validate([
            'name' => 'required|string|max:255',
            'slug' => 'required|string|unique:products,slug',
            'description' => 'nullable|string',
            'price' => 'required|numeric|min:0',
            'compare_at_price' => 'nullable|numeric|min:0',
            'cost' => 'nullable|numeric|min:0',
            'quantity' => 'required|integer|min:0',
            'sku' => 'nullable|string|unique:products,sku',
            'category_id' => 'nullable|exists:categories,id',
            'collection_id' => 'nullable|exists:collections,id',
            'is_active' => 'boolean',
            'is_featured' => 'boolean',
        ]);

        Product::create($validated_data);

        return redirect()
            ->route('admin.products.index')
            ->with('success', 'Product created successfully');
    }

